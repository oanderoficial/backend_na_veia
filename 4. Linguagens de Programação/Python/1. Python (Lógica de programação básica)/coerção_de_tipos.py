# String → Inteiro
numero = int("42")

# String → Float
altura = float("1.75")

# Inteiro → String
texto = str(100)

# Número → Booleano
verdadeiro = bool(1)
falso = bool(0)

# Float → Inteiro (perde a parte decimal)
inteiro = int(9.81)

print(type(numero))
print(type(altura))
print(type(texto))
print(type(verdadeiro))
print(type(falso))
print(type(inteiro))