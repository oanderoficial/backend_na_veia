"""
| Operador | Significado    | Exemplo  |
| -------- | -------------- | -------- |
| `==`     | igual a        | `a == b` |
| `!=`     | diferente de   | `a != b` |
| `>`      | maior que      | `a > b`  |
| `<`      | menor que      | `a < b`  |
| `>=`     | maior ou igual | `a >= b` |
| `<=`     | menor ou igual | `a <= b` |

"""

a = 10
b = 5

print(a > b)   # True
print(a == b)  # False
print(a <= 10) # True
print(a != 7)  # True
